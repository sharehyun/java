package ja0721;

public class VarEx {

	public static void main(String[] args) {
		int year = 0;
		int age = 14;
		
		System.out.println("year : "+year);
		System.out.println("age : "+age);
		
		year = age + 2000;
		age = age + 1;
		
		System.out.println("year : "+year);
		System.out.println("age : "+age);

	}

}
